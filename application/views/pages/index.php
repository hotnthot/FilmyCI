        <h3>Informace</h3>
        <p style="color: black">Nové moderní kino Hvězda vyprojektoval brněnský architekt Zdeněk Michal jako víceúčelový objekt s nadzemní (prostorní foyer, kavárna s vinárnou, provozní místnosti) a podzemní částí (kinosál s 510 sedadly a širokoúhlým plátnem parabolického tvaru). Kino bylo slavnostně otevřeno v pátek 6. října 1967 uvedením filmu Markéta Lazarová.
          <br>
          V roce 1981 se stává Uherské Hradiště pořadatelem Letní filmové školy, avšak po roce 1984 - vzhledem k tehdejší kulturní politice - bylo nuceno na několik let "filmovku" oželet. Od roku 1992 je už město natrvale spojené s tímto fenoménem, který už řadu let organizují Městská kina ve spolupráci s Asociací českých filmových klubů a Národním filmovým archivem. Bohatý odborný i doprovodní program (tematické přednášky, besedy, workshopy, semináře, masterclass, diskuzní panely, koncertní a divadelní vystoupení) přiláká ročně do města tisícovky návštěvníků. Zrodil se tady i Projekt 100, Seminář britského filmu, projekt Film a škola, Kino Art, Zlatý fond, Bijásek a Malovásek, Baby Bio nebo Bio Senior. Od roku 2006 se kino Hvězda řadí mezi členy prestižní sítě Europa Cinemas.
          <br>
          Ač byla budova i kinosál Hvězdy značně poškozeny ničivou povodní v roce 1997, generální rekonstrukce (podle projektu uherskohradišťského ateliéru GG ARCHICO) se dočkaly až o 12 let později. Novou podobu získalo foyer i kinosál, v respiriu vzniklo malé podium s hledištěm a v suterénu víceúčelový klub včetně zázemí. Za podpory fondů Evropské unie se rozšířily možnosti využití Městského kulturního centra Hvězda o pořádání konferencí, seminářů, dramatických a hudebních produkcí.
